# Instrucciones: Calcular el area y perimetro de un cuadrado
base = 5
altura = 7
metro = 100
pulgada = 2.54
area = base*altura
perimetro = 2*(base+altura)
print('El area es de '+str(area)+ ' y el perimetro es de '+str(perimetro)+' centimetros')
print('El area es de '+str((area)/metro)+ ' y el perimetro es de '+str((perimetro)/metro)+' metros')
print('El area es de '+str((area)/pulgada)+ ' y el perimetro es de '+str((perimetro)/pulgada)+' pulgadas')
